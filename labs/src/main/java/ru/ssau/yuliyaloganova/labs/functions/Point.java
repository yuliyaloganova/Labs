package ru.ssau.yuliyaloganova.labs.functions;

public class Point {
    public final double x;
    public final double y;

    public Point(double x, double y){
        this.x=x;
        this.y=y;
    }

    public double getX() {
        return x;
    }

    public  double getY(){
        return y;
    }
}
